/* 
The program explains Arithmetic Operations 
Usage: operators - '+','-','*','/','%'
Syntax:operand <operator> operand - a+b
*/
#include<stdio.h>
int main(){
    int x=20,y=10;
    printf("The sum is :%d\n",x+y);
    printf("The difference is :%d\n",x-y);
    printf("The product is :%d\n",x*y);
    printf("The division is :%d\n",x/y);
    printf("The modulus is :%d\n",x%y);
}